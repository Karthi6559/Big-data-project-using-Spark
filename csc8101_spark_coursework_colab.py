# Solution to task 3.2
def t32_summarise_zones_pairs(df, zones_df=zone_names):

    # Aggregate outgoing edges per zone
    zone_summary = (df.groupBy("PULocationID").agg(F.sum("trips_count").alias("total_trips"),F.avg("average_unit_profit").alias("avg_profit"),
                          F.sum("total_passengers").alias("total_passengers")
                      ))

    # Join zone names
    zone_summary = (zone_summary.join(zones_df, zone_summary.PULocationID == zones_df.LocationID, "left").drop("LocationID"))

    # Rename zone name column
    zone_summary = zone_summary.withColumnRenamed("Zone", "Zone_Name")

    return zone_summary

# Top 10 ranked zones by traffic (trip volume)
def t32_top10_trips(df_zones):
    return (df_zones.orderBy(F.desc("total_trips"))
                    .limit(10)
                    .toPandas())

# Top 10 ranked zones by profit
def t32_top10_profit(df_zones):
    return (df_zones.orderBy(F.desc("avg_profit"))
                    .limit(10)
                    .toPandas())

# Top 10 ranked zones by passenger volume
def t32_top10_passenger(df_zones):
    return (df_zones.orderBy(F.desc("total_passengers"))
                    .limit(10)
                    .toPandas())

