# Task 2.1 - Join zone names
def t21_join_zones(df, zones_df=zone_names):
    zones_pu = zones_df.select(F.col("LocationID").alias("PU_ID"),F.col("Zone").alias("PU_Zone"))

# Use PU_Zone and DO_Zone to avoid ambiguity
    zones_do = zones_df.select(F.col("LocationID").alias("DO_ID"),F.col("Zone").alias("DO_Zone"))

# Use left join so that even if LocationID is missing we won't loose trip record
    # 1) Join on PULocationID with PU_Zone
    df1 = df.join(zones_pu, df.PULocationID == zones_pu.PU_ID, "left")

    # 2) Join on DOLocationID with DO_Zone
    df2 = df1.join(zones_do, df1.DOLocationID == zones_do.DO_ID, "left")

    # Drop duplicate ID columns from zone names lookup
    df_final = df2.drop("PU_ID").drop("DO_ID")

    return df_final

