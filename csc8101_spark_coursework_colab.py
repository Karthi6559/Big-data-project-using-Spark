def t12_remove_outliers(df):

    # Trip_distance

    median = df.agg(
        pf.percentile_approx("trip_distance", 0.5, 10000)
    ).collect()[0][0]

    df = df.withColumn(
        "subtractedTrip",
        pf.abs(pf.col("trip_distance") - pf.lit(median))
    )

    madTrip = 1.483 * df.agg(
        pf.percentile_approx("subtractedTrip", 0.5, 10000)
    ).collect()[0][0]

    df = df.withColumn("madTrip", pf.lit(madTrip))

    df = df.withColumn(
        "zscoreTrip",
        pf.col("subtractedTrip") / pf.lit(madTrip)
    )


    # Total_amount

    totalMedian = df.agg(
        pf.percentile_approx("total_amount", 0.5, 10000)
    ).collect()[0][0]

    df = df.withColumn(
        "subtractedAmount",
        pf.abs(pf.col("total_amount") - pf.lit(totalMedian))
    )

    madTotal = 1.483 * df.agg(
        pf.percentile_approx("subtractedAmount", 0.5, 10000)
    ).collect()[0][0]

    df = df.withColumn("madAmount", pf.lit(madTotal))

    df = df.withColumn(
        "zscoreAmount",
        pf.col("subtractedAmount") / pf.lit(madTotal)
    )


    # Filter

    df = df.filter(
        (pf.col("zscoreTrip") < 3.5) &
        (pf.col("zscoreAmount") < 3.5)
    )

    df = df.drop(
        "subtractedTrip",
        "madTrip",
        "zscoreTrip",
        "subtractedAmount",
        "madAmount",
        "zscoreAmount"
    )

    return df

