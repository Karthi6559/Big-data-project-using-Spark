# Task 3.1 - Summarise interzonal travel
def t31_summarise_trips(df):
    # Group by origin and destination zones
    graph = (df.groupBy("PULocationID", "DOLocationID")
               .agg(F.avg("unit_profitability").alias("average_unit_profit"),
                    F.count("*").alias("trips_count"), F.sum("passenger_count").alias("total_passengers")))
    return graph

